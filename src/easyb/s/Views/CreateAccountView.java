    private void createAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountButtonActionPerformed
        //When this button is clicked, the system proceeds to confirm if the electronic 
        //mail that is being used to create the new account is already registered in an 
        //already made account, if this happens the system notifies the new user that the 
        //mail is already in use. If the email is not in use, the system will save the new 
        //user to the system.

        if (fullNameTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define a NAME to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.fullNameTextField.requestFocus();
            return;
            
        }
        
        if (idTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define an ID to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.idTextField.requestFocus();
            return;
            
        }
        
        if (ageTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define an AGE to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.ageTextField.requestFocus();
            return;
            
        }
        
        if (emailTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define an EMAIL to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.emailTextField.requestFocus();
            return;
            
        }
        
        if (countryTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define a COUNTRY to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.countryTextField.requestFocus();
            return;
            
        }
        
        if (passWordTextField.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "You must define a PASSWORD to create an account",
                    "Problem creating user account", JOptionPane.ERROR_MESSAGE);
            this.passWordTextField.requestFocus();
            return;
            
        }

        String typeUser = selectUserComboBox.getSelectedItem().toString();
        
        for (User emailUser : Global.usersList){
            
            String email = emailUser.getEmail();
            if (email.equals(emailTextField.getText())){
                
                JOptionPane.showMessageDialog(this, "The email " + emailTextField.getText() + 
                        " is already used in another account",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }  
            
        }

        if (typeUser.equals("Buyer")) {

            String name = fullNameTextField.getText();
            int id = Integer.parseInt(idTextField.getText());
            int age = Integer.parseInt(ageTextField.getText());
            String email = emailTextField.getText();
            String country = countryTextField.getText();
            String passWord = passWordTextField.getText();

            Buyer newBuyer = new Buyer(name, id, age, email, country, typeUser, passWord);
            Global.usersList.add(newBuyer);

            cleanTextFields();
            
            System.out.println("The size of the user list is: " + Global.usersList.size());
            
            System.out.println("You created an account on EasyB&S this date: ");
            newBuyer.getShowTime();

            System.out.println(newBuyer.getInformation() + "\n" + "* ** *** **** | **** *** ** *");

        } else {

            String commercialReason = commercialReasonTextField.getText();
            String name = fullNameTextField.getText();
            int id = Integer.parseInt(idTextField.getText());
            int age = Integer.parseInt(ageTextField.getText());
            String email = emailTextField.getText();
            String country = countryTextField.getText();
            String passWord = passWordTextField.getText();

            Seller newSeller = new Seller(commercialReason, name, id, age, email, country, typeUser, passWord);
            Global.usersList.add(newSeller);
            
            cleanTextFields();
            
            System.out.println("The size of the user list is: " + Global.usersList.size());

            System.out.println("You created an account on EasyB&S this date: ");
            newSeller.getShowTime();
            
            System.out.println(newSeller.getInformation() + "\n" + "* ** *** **** | **** *** ** *");

        }

       this.dispose();
    
    }//GEN-LAST:event_createAccountButtonActionPerformed

