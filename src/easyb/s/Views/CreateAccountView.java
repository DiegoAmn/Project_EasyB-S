    //If the user is a seller a new TextField is allowed so that it can type inside it.
    public void checkTypeUser() {

        String typeUser = selectUserComboBox.getSelectedItem().toString();

        if (typeUser.equals("Buyer")) {

            commercialReasonTextField.setEditable(false);

        } else {

            commercialReasonTextField.setEditable(true);

        }

    }

